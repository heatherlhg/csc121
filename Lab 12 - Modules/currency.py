def to_euro(dollar):
    """ Convert USD to EURO """
    euro = dollar * 0.81

    return euro

def to_yen(dollar):
    """ Convert USD to JPY """
    yen = dollar * 106.45

    return yen

def to_peso(dollar):
    """ Convert USD to MXN"""
    peso = dollar * 18.58

    return peso
